package Code05_02;

public class Main {
	//メソッドの呼び出しP186
	public static void main(String[] args) {
		System.out.println("メソッドを呼び出します");
//	hellpメソッドを呼び出す
		hello();
		System.out.println("メソッド呼び出し終了");
	}
	
//		メソッドの定義,hellpメソッドの中身
	public static void hello() {
		System.out.println("もりさん、やっほー");
	}
}
